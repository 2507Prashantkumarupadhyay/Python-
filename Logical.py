# Logical Operators:

"""
1.Logical Operator perform Logical AND, Logical OR and Logical NOT Operations.
2.It is used to combine conditional statements.
"""
# Examples of Logical Operator
a = True
b = False

# Print a and b is False
print(a and b)

# Print a or b is True
print(a or b)

# Print not a is False
print(not a)
